// Adição 

let valor = 10;

let segundoValor = 20;


console.log(valor + segundoValor);

let mensagem = "20";

console.log(mensagem + valor); // concatenação

// Subtração

console.log(segundoValor - valor);

// Divisão

console.log(valor / segundoValor);
console.log(segundoValor / valor);


// modulo 

console.log(segundoValor % valor);

// Operadores aritimeticos avançados
//exponenciaçao **
let base = 2;
let expoente = 3;
let resultado = base ** expoente;

console.log(resultado);

// Incremento ++ (add 1)

let controle = 5;

console.log(controle++);
console.log(controle);

console.log(controle--);
console.log(controle);

console.log(++controle);
console.log(--controle);

